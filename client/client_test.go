// TestWithName ensures that an explicitly passed name is used in leau of the
// path derived name when provide, and persists through instantiations.
// This also ensures that an initialized service function's name persists if
// the path is changed after creation.
func TestWithName(t *testing.T) {
	// Create the directory of the new service function.
	root := "testdata/example.com/groupA/service"
	os.MkdirAll(root, 0700)
	defer os.RemoveAll(root)

	initializer := mock.NewInitializer()
	c, err := client.New(root,
		client.WithName("service.example.com"), // specify a name override.
		client.WithInitializer(initializer),
	)
	if err != nil {
		t.Fatal(err)
	}

	// Ensure that initializing receives the specified name.
	initializer.InitializeFn = func(name, language, path string) error {
		if name != "service.example.com" {
			t.Fatalf("initializer expected name 'service.example.com', got '%v'", name)
		}
		return nil
	}

	// A supported langauge should not error.
	if err := c.Create("go"); err != nil {
		t.Fatal(err)
	}

	// Create a new client in the same directory, which should pick up the same name.
	updater := mock.NewUpdater()
	c, err = client.New(root, // will not be used for name derivation as a config now exists.
		client.WithUpdater(updater),
	)
	if err != nil {
		t.Fatal(err)
	}

	// Ensure that updating takes place using the previously initialized name
	updater.UpdateFn = func(name, image string) error {
		if name != "service.example.com" {
			t.Fatalf("updater expected name 'service.example.com', got '%v'", name)
		}
		return nil
	}

	// Invoke update
	if err := c.Update(); err != nil {
		t.Fatal(err)
	}

}
