// File-mangling constants shared with the hack/cmd/embd tool.
const (
	// EmbdSuffix is appended to go.mod and go.sum filenames so that go:embed
	// does not skip the directory (go:embed silently skips dirs with go.mod).
	EmbdSuffix = ".embd"

	// SymlinkSuffix is appended to symlink marker files. A file named
	// "foo.symlink" is a plain-text file whose content is the symlink target.
	// go:embed silently drops real symlinks, so we represent them this way.
	SymlinkSuffix = ".symlink"

	// EmbdBuildConstraint is the build-constraint tag name used to mark Go
	// source files inside templates/ so they are not compiled by go build.
	EmbdBuildConstraint = "embd"

	// PermissionsManifest is the name of the file listing non-default
	// permissions in the templates/ tree, generated by hack/cmd/permissiongen.
	PermissionsManifest = ".permissions"

	permissionsManifest = PermissionsManifest
)

// manglingFS wraps a Filesystem and transparently reverses on-disk name
// mangling applied to work around go:embed limitations:
//
//   - go.mod.embd / go.sum.embd  →  go.mod / go.sum
//     (go:embed skips directories containing go.mod; renaming avoids this)
//
//   - foo.symlink (regular file containing target path)  →  foo (symlink entry)
//     (go:embed silently drops symlinks; .symlink files are a workaround)
//
// Additionally, manglingFS:
//   - strips "//go:build embd" lines from file content so users never see them
//   - restores file permissions from .permissions manifest (embed.FS drops them)
//   - returns 0644 for regular files and 0755 for directories by default
type manglingFS struct {
	inner Filesystem
	// perms maps relative path → permission bits for files with non-default perms.
	// Default assumed for files: 0644. For dirs: 0755. Loaded from .permissions.
	perms map[string]fs.FileMode
}

// NewManglingFS creates a manglingFS wrapping the given embed.FS.
func NewManglingFS(inner embed.FS) manglingFS {
	adapted := embedFSAdapter{inner}
	return manglingFS{inner: adapted, perms: loadPerms(adapted)}
}

// NewManglingFilesystem creates a manglingFS wrapping any Filesystem.
// This is useful for testing with osFilesystem or billyFilesystem backends
// that serve a templates directory containing on-disk mangled files.
func NewManglingFilesystem(inner Filesystem) manglingFS {
	return manglingFS{inner: inner, perms: loadPerms(inner)}
}

// loadPerms reads the .permissions manifest from the filesystem and returns
// a map of path → permission bits for non-default files.
func loadPerms(fsys Filesystem) map[string]fs.FileMode {
	f, err := fsys.Open(permissionsManifest)
	if err != nil {
		return nil
	}
	defer f.Close()

	perms := make(map[string]fs.FileMode)
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		var mode uint32
		var path string
		if n, err := parsePermLine(line, &mode, &path); n == 2 && err == nil {
			perms[path] = fs.FileMode(mode)
		}
	}
	return perms
}

// parsePermLine parses a line like "0755 quarkus/cloudevents/mvnw".
func parsePermLine(line string, mode *uint32, path *string) (int, error) {
	var n int
	var err error
	_, err = func() (int, error) {
		parts := strings.SplitN(line, " ", 2)
		if len(parts) != 2 {
			return 0, io.ErrUnexpectedEOF
		}
		var m uint32
		for _, c := range parts[0] {
			if c < '0' || c > '7' {
				return 0, io.ErrUnexpectedEOF
			}
			m = m*8 + uint32(c-'0')
		}
		*mode = m
		*path = strings.TrimSpace(parts[1])
		n = 2
		return 2, nil
	}()
	return n, err
}

// permFor returns the effective permission for the named path (relative).
// Defaults to 0644 for files; .permissions entries override this.
func (m manglingFS) permFor(name string) fs.FileMode {
	if perm, ok := m.perms[name]; ok {
		return perm
	}
	return 0644
}

// Open opens the named file. If the file is not found, it also tries
// name + ".embd" (for go.mod / go.sum files).
func (m manglingFS) Open(name string) (fs.File, error) {
	f, err := m.inner.Open(name)
	if err != nil {
		// Try with .embd suffix (handles go.mod.embd, go.sum.embd)
		f2, err2 := m.inner.Open(name + ".embd")
		if err2 != nil {
			return nil, err // return original error
		}
		return &embdFile{File: f2, name: name, perm: m.permFor(name)}, nil
	}
	return &strippedFile{File: f, perm: m.permFor(name)}, nil
}

// Stat returns FileInfo for the named file.
func (m manglingFS) Stat(name string) (fs.FileInfo, error) {
	fi, err := m.inner.Stat(name)
	if err == nil {
		return &permFileInfo{FileInfo: fi, perm: m.permFor(name)}, nil
	}
	// Try .embd suffix
	fi2, err2 := m.inner.Stat(name + ".embd")
	if err2 == nil {
		return &mangledFileInfo{FileInfo: fi2, name: name, perm: m.permFor(name)}, nil
	}
	// Try .symlink suffix
	fi3, err3 := m.inner.Stat(name + ".symlink")
	if err3 == nil {
		return &symlinkFileInfo{FileInfo: fi3, name: name}, nil
	}
	return nil, err
}

// ReadDir lists the directory named by name, reversing name mangling on entries.
func (m manglingFS) ReadDir(name string) ([]fs.DirEntry, error) {
	entries, err := m.inner.ReadDir(name)
	if err != nil {
		return nil, err
	}
	result := make([]fs.DirEntry, 0, len(entries))
	for _, e := range entries {
		n := e.Name()

		// Build the relative path for permission lookup
		var rel string
		if name == "." || name == "" {
			rel = n
		} else {
			rel = name + "/" + n
		}

		switch {
		case strings.HasSuffix(n, ".embd"):
			// Strip .embd suffix — present go.mod / go.sum with real name
			bareRel := strings.TrimSuffix(rel, ".embd")
			result = append(result, &mangledDirEntry{
				DirEntry: e,
				name:     strings.TrimSuffix(n, ".embd"),
				perm:     m.permFor(bareRel),
			})
		case strings.HasSuffix(n, ".symlink"):
			// Present as a symlink DirEntry with the bare name
			result = append(result, &symlinkDirEntry{
				DirEntry: e,
				name:     strings.TrimSuffix(n, ".symlink"),
			})
		default:
			result = append(result, &permDirEntry{DirEntry: e, perm: m.permFor(rel)})
		}
	}
	return result, nil
}

// Readlink returns the target of the symlink at the given path by reading
// the corresponding .symlink marker file.
func (m manglingFS) Readlink(link string) (string, error) {
	f, err := m.inner.Open(link + ".symlink")
	if err != nil {
		return "", &fs.PathError{Op: "readlink", Path: link, Err: fs.ErrInvalid}
	}
	defer f.Close()
	bs, err := io.ReadAll(f)
	if err != nil {
		return "", err
	}
	// Trim trailing newline added to satisfy EOF-newline linting requirements.
	return strings.TrimRight(string(bs), "\n\r"), nil
}

// ---------------------------------------------------------------------------
// embedFSAdapter adapts embed.FS to the Filesystem interface.
// ---------------------------------------------------------------------------

type embedFSAdapter struct {
	inner embed.FS
}

func (a embedFSAdapter) Open(name string) (fs.File, error) {
	return a.inner.Open(name)
}

func (a embedFSAdapter) ReadDir(name string) ([]fs.DirEntry, error) {
	return a.inner.ReadDir(name)
}

func (a embedFSAdapter) Stat(name string) (fs.FileInfo, error) {
	f, err := a.inner.Open(name)
	if err != nil {
		return nil, err
	}
	defer f.Close()
	return f.Stat()
}

func (a embedFSAdapter) Readlink(link string) (string, error) {
	return "", &fs.PathError{Op: "readlink", Path: link, Err: fs.ErrInvalid}
}

