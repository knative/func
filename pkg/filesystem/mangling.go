// strippedFile strips "//go:build embd" build-constraint lines from file
// content. This prevents the constraint from appearing in scaffolded files.
type strippedFile struct {
	fs.File
	perm     fs.FileMode
	stripped bool
	buf      *bytes.Reader
}

func (s *strippedFile) Read(p []byte) (int, error) {
	if !s.stripped {
		raw, err := io.ReadAll(s.File)
		if err != nil {
			return 0, err
		}
		raw = stripEmbdBuildTag(raw)
		s.buf = bytes.NewReader(raw)
		s.stripped = true
	}
	return s.buf.Read(p)
}

func (s *strippedFile) Stat() (fs.FileInfo, error) {
	fi, err := s.File.Stat()
	if err != nil {
		return nil, err
	}
	return &permFileInfo{FileInfo: fi, perm: s.perm}, nil
}

// StripEmbdBuildTag removes the "embd" tag from the //go:build constraint in
// Go source bytes, using the go/build/constraint package for correct AST
// manipulation. It handles all forms:
//
//   - "//go:build embd"            → line (+ following blank line) removed
//   - "//go:build embd && <rest>"  → replaced with "//go:build <rest>"
//   - "//go:build embd || <rest>"  → replaced with "//go:build <rest>"
//   - any deeper nesting           → embd node pruned from the AST
//
// Lines that do not contain the "embd" tag are kept byte-for-byte unchanged.
func StripEmbdBuildTag(src []byte) []byte {
	lines := bytes.SplitAfter(src, []byte("\n"))
	out := make([]byte, 0, len(src))
	skipNext := false
	for _, line := range lines {
		trimmed := strings.TrimRight(string(line), "\r\n")
		if skipNext && trimmed == "" {
			skipNext = false
			continue
		}
		skipNext = false

		if !constraint.IsGoBuild(trimmed) {
			out = append(out, line...)
			continue
		}
		// Parse the //go:build line.
		expr, err := constraint.Parse(trimmed)
		if err != nil || !containsEmbdTag(expr) {
			// Not parseable or does not contain "embd" — keep unchanged.
			out = append(out, line...)
			continue
		}
		pruned := removeEmbdTag(expr)
		if pruned == nil {
			// The only tag was "embd" — drop the line and the blank after it.
			skipNext = true
			continue
		}
		out = append(out, []byte("//go:build "+pruned.String()+"\n")...)
	}
	return out
}

// AddEmbdBuildTag prepends or merges the "embd" build constraint into Go
// source bytes. If the file already has a //go:build line, the new constraint
// is ANDed with the existing one. Otherwise "//go:build embd\n\n" is prepended.
func AddEmbdBuildTag(src []byte) []byte {
	lines := bytes.SplitAfter(src, []byte("\n"))
	for i, line := range lines {
		trimmed := strings.TrimRight(string(line), "\r\n")
		if !constraint.IsGoBuild(trimmed) {
			continue
		}
		// Found an existing //go:build line — parse and merge.
		existing, err := constraint.Parse(trimmed)
		if err != nil {
			break
		}
		merged := &constraint.AndExpr{X: &constraint.TagExpr{Tag: EmbdBuildConstraint}, Y: existing}
		lines[i] = []byte("//go:build " + merged.String() + "\n")
		return bytes.Join(lines, nil)
	}
	// No existing constraint — prepend "//go:build embd\n\n".
	header := []byte("//go:build " + EmbdBuildConstraint + "\n\n")
	return append(header, src...)
}

// containsEmbdTag reports whether the expression tree contains the "embd" tag.
func containsEmbdTag(expr constraint.Expr) bool {
	switch e := expr.(type) {
	case *constraint.TagExpr:
		return e.Tag == EmbdBuildConstraint
	case *constraint.AndExpr:
		return containsEmbdTag(e.X) || containsEmbdTag(e.Y)
	case *constraint.OrExpr:
		return containsEmbdTag(e.X) || containsEmbdTag(e.Y)
	case *constraint.NotExpr:
		return containsEmbdTag(e.X)
	}
	return false
}

// removeEmbdTag recursively removes all occurrences of the "embd" Tag from a
// constraint.Expr AST. Returns nil when the entire expression collapses.
func removeEmbdTag(expr constraint.Expr) constraint.Expr {
	switch e := expr.(type) {
	case *constraint.TagExpr:
		if e.Tag == EmbdBuildConstraint {
			return nil
		}
		return e
	case *constraint.AndExpr:
		x := removeEmbdTag(e.X)
		y := removeEmbdTag(e.Y)
		if x == nil {
			return y
		}
		if y == nil {
			return x
		}
		return &constraint.AndExpr{X: x, Y: y}
	case *constraint.OrExpr:
		x := removeEmbdTag(e.X)
		y := removeEmbdTag(e.Y)
		if x == nil {
			return y
		}
		if y == nil {
			return x
		}
		return &constraint.OrExpr{X: x, Y: y}
	case *constraint.NotExpr:
		inner := removeEmbdTag(e.X)
		if inner == nil {
			return nil
		}
		return &constraint.NotExpr{X: inner}
	}
	return expr
}

// stripEmbdBuildTag is the package-internal alias for StripEmbdBuildTag.
func stripEmbdBuildTag(src []byte) []byte { return StripEmbdBuildTag(src) }

// embdFile wraps a file that was opened with a .embd suffix but
// exposes the original (non-.embd) name in Stat().
type embdFile struct {
	fs.File
	name     string
	perm     fs.FileMode
	stripped bool
	buf      *bytes.Reader
}

func (e *embdFile) Read(p []byte) (int, error) {
	if !e.stripped {
		raw, err := io.ReadAll(e.File)
		if err != nil {
			return 0, err
		}
		raw = stripEmbdBuildTag(raw)
		e.buf = bytes.NewReader(raw)
		e.stripped = true
	}
	return e.buf.Read(p)
}

func (e *embdFile) Stat() (fs.FileInfo, error) {
	fi, err := e.File.Stat()
	if err != nil {
		return nil, err
	}
	return &mangledFileInfo{FileInfo: fi, name: e.name, perm: e.perm}, nil
}

// permFileInfo wraps fs.FileInfo and overrides Mode() to apply correct perm bits.
type permFileInfo struct {
	fs.FileInfo
	perm fs.FileMode
}

func (p *permFileInfo) Mode() fs.FileMode {
	base := p.FileInfo.Mode()
	if base.IsDir() {
		return base.Type() | 0755
	}
	if base.Type()&fs.ModeType != 0 {
		return base // preserve special types (symlinks etc.)
	}
	return base.Type() | p.perm
}

// mangledFileInfo wraps fs.FileInfo but returns a custom name and permission.
type mangledFileInfo struct {
	fs.FileInfo
	name string
	perm fs.FileMode
}

func (m *mangledFileInfo) Name() string { return m.name }
func (m *mangledFileInfo) Mode() fs.FileMode {
	base := m.FileInfo.Mode()
	if base.IsDir() {
		return base.Type() | 0755
	}
	return base.Type() | m.perm
}

// symlinkFileInfo wraps fs.FileInfo, reports a custom name and ModeSymlink.
type symlinkFileInfo struct {
	fs.FileInfo
	name string
}

func (s *symlinkFileInfo) Name() string       { return s.name }
func (s *symlinkFileInfo) Mode() fs.FileMode  { return fs.ModeSymlink | 0777 }
func (s *symlinkFileInfo) Size() int64        { return 0 }
func (s *symlinkFileInfo) ModTime() time.Time { return s.FileInfo.ModTime() }
func (s *symlinkFileInfo) IsDir() bool        { return false }
func (s *symlinkFileInfo) Sys() interface{}   { return nil }

// permDirEntry wraps a DirEntry and applies correct permission on Info().
type permDirEntry struct {
	fs.DirEntry
	perm fs.FileMode
}

func (p *permDirEntry) Info() (fs.FileInfo, error) {
	fi, err := p.DirEntry.Info()
	if err != nil {
		return nil, err
	}
	return &permFileInfo{FileInfo: fi, perm: p.perm}, nil
}

